    private void generateBarcodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateBarcodeActionPerformed
        // TODO add your handling code here:
        String text1 = text.getText();
        if (text1.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter the text !!!");
        }
        try{
            barcodeImage = createBarCode(text1,100,300);
            barcode_image.setIcon(new ImageIcon(barcodeImage));
            
        } catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
        
        if(barcode_image != null){
            JFileChooser jf = new JFileChooser();
            int result = jf.showOpenDialog(this);
            if (result == JFileChooser.APPROVE_OPTION){
                File file = jf.getSelectedFile();
                try{
                   ImageIO.write(barcodeImage, "png", file);
                   JOptionPane.showMessageDialog(this, "File Saved Sucessfully !!!");
                } 
                catch (IOException e){
                    JOptionPane.showMessageDialog(this, e);
                }
            }
        }
             
    }//GEN-LAST:event_generateBarcodeActionPerformed


    private BufferedImage createBarCode(String txt, int height, int width) throws Exception{
         BitMatrix bitMatrix = new MultiFormatWriter().encode(txt, BarcodeFormat.CODE_128, width, height);
         return MatrixToImageWriter.toBufferedImage(bitMatrix);
    }
    

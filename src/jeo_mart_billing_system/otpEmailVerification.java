    protected void sendOtp(String receiverEmail, String otp){
        String fromEmail = sender.getText();
        String pass = password.getText();
        
        Properties prop = new Properties();
        prop.put("mail.smtp.host", "smtp.gmail.com");
        prop.put("mail.smtp.port", 587);
        prop.put("mail.smtp.auth","true");
        prop.put("mail.smtp.starttls.enable","true");
        
        Session session = Session.getInstance(prop, new javax.mail.Authenticator(){
            @Override
            protected PasswordAuthentication getPasswordAuthentication(){
                return new PasswordAuthentication(fromEmail,pass);
            }
        });
        
        try{
            MimeMessage mime = new MimeMessage(session);
            
            mime.setFrom(new InternetAddress(fromEmail));
            mime.setRecipient(Message.RecipientType.TO, new InternetAddress(receiverEmail));
//            mime.setRecipient(Message.RecipientType.TO, InternetAddress.parse(receiverEmail));
            mime.setSubject("Your OTP Code");
            mime.setText("Your OTP code for Verification is: "+otp);
            Transport.send(mime);
            JOptionPane.showMessageDialog(this,"OTP sent !!");
        }catch(MessagingException e){
             JOptionPane.showMessageDialog(this,e);
        }
        
    }
    
    protected String generateOtp(){
        int otp = (int) (Math.random() * 900000) + 100000;
        return String.valueOf(otp);
    }

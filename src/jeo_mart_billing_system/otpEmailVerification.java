    private void send_otpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_send_otpActionPerformed
        // TODO add your handling code here:
        String toEmail = receiver.getText();
        if (toEmail.isEmpty()) 
        {
            JOptionPane.showMessageDialog(this, "Enter a valid email !!");
        }
        else 
        {
            String regex = "^[a-zA-Z0-9]+(?:\\.[a-zA-Z0-9]+)*@(?:[a-z]+\\.)+[a-z]{2,3}";
            Pattern pattern = Pattern.compile(regex);
            Matcher matcher = pattern.matcher(toEmail);
            if (matcher.matches()) {
                JOptionPane.showMessageDialog(this, "valid receiver Email");
                generatedOtp = generateOtp();
                sendOtp(toEmail, generatedOtp);
            } else {
                JOptionPane.showMessageDialog(this, "Invalid receiver Email");
            }

        }

    }//GEN-LAST:event_send_otpActionPerformed

    private void verifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verifyActionPerformed
        // TODO add your handling code here:
        String enteredOtp = otp.getText();
        if (enteredOtp.equals(generatedOtp)) {
            JOptionPane.showMessageDialog(this, "Verification Sucess !!!");
            this.dispose();
             new main_page().setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(this, "Wrong OTP !! Verification Failed ");
        }

    }//GEN-LAST:event_verifyActionPerformed

